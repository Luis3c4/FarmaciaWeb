public class Producto {
    private int id;
    private String nombre;
    private double precio;
    private int stock;
    private String image;

    public Producto() {
        
    }

    public Producto(int id, String nombre, double precio, int stock, String image) {
        this.id=id;
        this.nombre = nombre;
        this.precio = precio;
        this.stock = stock;
        this.image= image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
    
}
